class Rectangle:
    def __init__(self, x, y, height, width):
        self.x = x
        self.y = y
        self.height = height
        self.width = width

    def getX(self):
        return self.x

    def getY(self):
        return self.y

    def getHeight(self):
        return self.height

    def getWidth(self):
        return self.width

    def setX(self, x):
        self.x = x

    def setY(self, y):
        self.y = y

    def setHeight(self, height):
        self.height = height

    def setWidth(self, width):
        self.width = width
